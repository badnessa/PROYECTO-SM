//Funcion que matiene la interfaz actualizada cuando ocurren eventos
//ingreso de nuevo usuario al canal o sala, actualiza la lista de usuarios, muestra un mensaje de bienvenida
let handleMemberJoined = async (MemberId) => {
    console.log('Un nuevo usuario ingreso a la sala', MemberId)
    addMemberToDom(MemberId)

    let members = await channel.getMembers()
    updateMemberTotal(members)

    let {name} = await rtmClient.getUserAttributesByKeys(MemberId, ['name'])
    addBotMessageToDom(`Bienvenido a la sal ${name}! ðŸ‘‹`)
}

//Funcion que agrega usuarios
let addMemberToDom = async (MemberId) => {
    let {name} = await rtmClient.getUserAttributesByKeys(MemberId, ['name'])

    let membersWrapper = document.getElementById('member__list')
    let memberItem = `<div class="member__wrapper" id="member__${MemberId}__wrapper">
                        <span class="green__icon"></span>
                        <p class="member_name">${name}</p>
                    </div>`

    membersWrapper.insertAdjacentHTML('beforeend', memberItem)
}

//Funcion que actualiza el numero de miembros que se unen al canal
let updateMemberTotal = async (members) => {
    let total = document.getElementById('members__count')
    total.innerText = members.length
}
 
//Funcion que actualiza el numero de miembros cuando alguien abandona el canal
let handleMemberLeft = async (MemberId) => {
    removeMemberFromDom(MemberId)

    let members = await channel.getMembers()
    updateMemberTotal(members)
}

//Funcion para mostrar mensaje de que un usuario ha abandonado la sala
let removeMemberFromDom = async (MemberId) => {
    let memberWrapper = document.getElementById(`member__${MemberId}__wrapper`)
    let name = memberWrapper.getElementsByClassName('member_name')[0].textContent
    addBotMessageToDom(`${name} abandono la sala`)
        
    memberWrapper.remove()
}

//Funcion que obtiene la cantidad de usuarios para luego poder actualizar
let getMembers = async () => {
    let members = await channel.getMembers()
    updateMemberTotal(members)
    for (let i = 0; members.length > i; i++){
        addMemberToDom(members[i])
    }
}

